const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Create Schema
const PlaylistSchema = new Schema({
  user: {
    type: Schema.Types.ObjectId,
    ref: "users"
  },
  name: {
    type: String,
    required: true
  },
  platform: {
    type: String,
    required: true
  },
  url: {
    type: String,
    required: true
  },
  likes: [
    {
      user: {
        type: Schema.Types.ObjectId,
        ref: "users"
      }
    }
  ],
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Playlist = mongoose.model("playlist", PlaylistSchema);
